let marks = prompt("Enter your marks");
if( marks >= 20){
    alert("Your grade is C")
}
if( marks >= 40){
    alert("Your grade is B")
}
if( marks >= 60){
    alert("Your grade is B+")
}
if( marks >= 80){
    alert("Your grade is A")
}
if( marks >= 90){
    alert("Your grade is A+")
}
else{
    alert("Please Enter your marks under 100")
}
